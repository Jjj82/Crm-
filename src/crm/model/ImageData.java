package crm.model;
/**
 * 上传图片实体类
 * @author 15056
 *
 */
public class ImageData {
	private String src;//图片路径
	private String title;//图片标题
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
